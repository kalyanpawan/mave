import java.util.*;
public class PositiveNegative {
    public static void main(String[] args) {
        double num;
        Scanner sc = new Scanner(System.in);
        num = sc.nextDouble();
        if (num < 0.0) {
            System.out.println("Negative!");
        }
        else{
            System.out.println("Negative!");
        }
        sc.close();
    }
}